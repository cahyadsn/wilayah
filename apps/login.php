<?php
session_start();

// Handle logout if requested (optional fallback or cleanup)
if (isset($_GET['logout'])) {
    session_destroy();
    header("Location: index.php");
    exit;
}

$error = '';
if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $username = $_POST['username'] ?? '';
    $password = $_POST['password'] ?? '';

    // Ensure environment variables are set to allow login
    $expectedUser = getenv('ADMIN_USER');
    $expectedPass = getenv('ADMIN_PASS');

    if ($expectedUser && $expectedPass && $username === $expectedUser && $password === $expectedPass) {
        $_SESSION['author'] = 'cahyadsn'; // Value expected by geo_update.php
        header("Location: index.php");
        exit;
    } else {
        $error = 'Invalid credentials';
    }
}
